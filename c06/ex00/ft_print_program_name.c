#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_str(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int	main(int argc, char **argv)
{
	if (argc <= 1)
	{
		ft_str(argv[0]);
		write(1, "\n", 1);
	}
	else
		write(1, "Erro 404", 9);
}
